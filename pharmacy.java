        DefaultTableModel mod = (DefaultTableModel)ph_table.getModel();
        
        name.setText(mod.getValueAt(ph_table.getSelectedRow(), 1).toString());
        for_.setSelectedItem(mod.getValueAt(ph_table.getSelectedRow(), 2).toString());
        price_p.setText(mod.getValueAt(ph_table.getSelectedRow(), 3).toString());
        price.setText(mod.getValueAt(ph_table.getSelectedRow(), 4).toString());
