    public static void load(){

        String url ="jdbc:mysql://localhost:3306/Project0" ;
        String user ="root";
        String pass ="Alora@19/7$";
        String sql ="select * from pharmacy_purchase;";
        
        try(Connection con = DriverManager.getConnection(url, user, pass)){
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            DefaultTableModel mod = (DefaultTableModel)ph_table.getModel();
            
            while(rs.next()){
                
                mod.addRow(new Object[]{rs.getInt("id"),rs.getString("name"),rs.getString("for_"),rs.getDouble("price_per_bar"),rs.getDouble("price"),rs.getInt("num_of_bar"),rs.getInt("qty")});
                
            }
            
            System.out.print("load done");
            
            
            
        }
        catch(SQLException ex){
            
        }
    }
    
    public void save(){
        
        String url ="jdbc:mysql://localhost:3306/project0" ;
        String user ="root";
        String pass ="Alora@19/7$";
        String sql ="insert into pharmacy_purchase( name, for_, price_per_bar, price, num_of_bar, qty) values(?,?,?,?,?,?);";
        
        DefaultTableModel mod = (DefaultTableModel)ph_table.getModel();
        
        try(Connection con = DriverManager.getConnection(url, user, pass)){
            
            PreparedStatement ps = con.prepareStatement(sql) ;
            ps.setString(1, name.getText());
            ps.setString(2, for_.getSelectedItem().toString());
            ps.setDouble(3, Double.parseDouble(price_p.getText()));
            ps.setDouble(4, Double.parseDouble(price.getText()));
            ps.setInt(5, Integer.parseInt(num_of_bars.getText()));
            ps.setInt(6, Integer.parseInt(qty.getText()));
            
            ps.execute();
            
            mod.setRowCount(0);
            load();
            
            System.out.print("save done");
        }catch(SQLException ex){
            
        }
    }
    
